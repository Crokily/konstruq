import type { ScheduleTask } from "@/lib/data/providers/interface";

interface ScheduleTaskSeed {
  key: string;
  name: string;
  startDate: string;
  finishDate: string;
  percentComplete: number;
  status: ScheduleTask["status"];
  parentKey?: string;
  isMilestone: boolean;
}

function buildProjectSchedule(
  projectId: string,
  seeds: ScheduleTaskSeed[]
): ScheduleTask[] {
  const ids = seeds.reduce<Record<string, string>>((acc, seed) => {
    acc[seed.key] = `${projectId}-${seed.key}`;
    return acc;
  }, {});

  return seeds.map((seed) => ({
    id: ids[seed.key],
    projectId,
    name: seed.name,
    startDate: seed.startDate,
    finishDate: seed.finishDate,
    percentComplete: seed.percentComplete,
    status: seed.status,
    parentId: seed.parentKey ? ids[seed.parentKey] ?? null : null,
    isMilestone: seed.isMilestone,
  }));
}

